#include<iostream>
using namespace std;
int main() {
	int p;
	cout << "Masukkan Bilangan:";
	cin >> p;

	if (p = 2 == 0) {
		cout << "Ganjil" << endl;
	}
	else {
		cout << "Genap" << endl;
	}
	if (p > 10) {
		cout << "Di Atas 10" << endl;
	}
	else {
		cout << "Di Bawah 10" << endl;
	}
	return 0;
}




